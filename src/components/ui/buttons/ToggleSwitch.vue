<template>
    <label class="switch">
        <input type="checkbox" :checked="value" @change="toggle">
        <span class="slider round"></span>
    </label>
</template>

<script>
export default {
    name: 'ToggleSwitch', // Component name
    props: ['value'],
    methods: {
        toggle() {
            this.$emit('update:value', !this.value);
        }
    }
}
</script>

